/* ─────────────────────────────────────────────────────
   SHARED HELPERS
───────────────────────────────────────────────────── */
function splitBullets(raw) {
  return (raw || '').split('\n').map(b => b.trim()).filter(Boolean);
}

function today() {
  return new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' });
}

/* ── jsPDF text wrapper ── */
function pdfAddWrappedText(doc, text, x, y, maxW, lineH) {
  const lines = doc.splitTextToSize(text, maxW);
  doc.text(lines, x, y);
  return y + lines.length * lineH;
}

/* ── Generic plain-text → PDF ── */
function buildPlainPDF(title, sections) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({ unit: 'pt', format: 'letter' });
  const marginL = 60, marginR = 60, pageW = 612, maxW = pageW - marginL - marginR;
  const pageH = 792, footerH = 40;
  let y = 70;

  const addPage = () => { doc.addPage(); y = 60; };
  const checkY = (need) => { if (y + need > pageH - footerH) addPage(); };

  // Title
  doc.setFont('helvetica', 'bold');
  doc.setFontSize(20);
  doc.setTextColor(17, 16, 9);
  doc.text(title, marginL, y); y += 32;

  // Subtitle line
  doc.setFont('helvetica', 'normal');
  doc.setFontSize(9);
  doc.setTextColor(125, 119, 105);
  doc.text(`Generated by CareerLaunch · ${today()}`, marginL, y); y += 24;

  // Rule
  const [ar, ag, ab] = (window.accentColor || {rgb:[200,151,58]}).rgb;
  doc.setDrawColor(ar, ag, ab);
  doc.setLineWidth(1.5);
  doc.line(marginL, y, pageW - marginR, y); y += 18;

  sections.forEach(sec => {
    if (!sec) return;
    checkY(40);

    // Section heading
    doc.setFont('helvetica', 'bold');
    doc.setFontSize(10);
    doc.setTextColor(ar, ag, ab);
    doc.text(sec.heading.toUpperCase(), marginL, y); y += 4;

    doc.setDrawColor(228, 222, 206);
    doc.setLineWidth(0.5);
    doc.line(marginL, y, pageW - marginR, y); y += 14;

    // Body lines
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(10);
    doc.setTextColor(17, 16, 9);

    (sec.lines || []).forEach(line => {
      if (!line) return;
      if (line.startsWith('•')) {
        checkY(16);
        const wrapped = doc.splitTextToSize(line, maxW - 12);
        doc.text(wrapped, marginL + 12, y);
        y += wrapped.length * 14;
      } else if (line.startsWith('__bold__')) {
        checkY(14);
        doc.setFont('helvetica', 'bold');
        doc.text(line.replace('__bold__', ''), marginL, y);
        doc.setFont('helvetica', 'normal');
        y += 14;
      } else {
        checkY(14);
        const wrapped = doc.splitTextToSize(line, maxW);
        doc.text(wrapped, marginL, y);
        y += wrapped.length * 14;
      }
    });
    y += 16;
  });

  // Footer
  const pageCount = doc.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFont('helvetica', 'normal');
    doc.setFontSize(8);
    doc.setTextColor(125, 119, 105);
    doc.text(`CareerLaunch · Page ${i} of ${pageCount}`, marginL, pageH - 20);
  }

  return doc;
}

/* ── Generic plain-text → DOCX ── */
function buildPlainDOCX(title, sections) {
  const { Document, Paragraph, TextRun, HeadingLevel, AlignmentType, BorderStyle, Packer } = docx;

  const children = [];

  // Title
  children.push(new Paragraph({
    children: [new TextRun({ text: title, bold: true, size: 40, color: '111009' })],
    spacing: { after: 100 },
  }));

  // Subtitle
  children.push(new Paragraph({
    children: [new TextRun({ text: `Generated by CareerLaunch · ${today()}`, size: 18, color: '7d7769' })],
    spacing: { after: 200 },
  }));

  sections.forEach(sec => {
    if (!sec) return;

    // Section heading
    children.push(new Paragraph({
      children: [new TextRun({ text: sec.heading.toUpperCase(), bold: true, size: 20, color: (window.accentColor || {hex:'#c8913a'}).hex.replace('#','') })],
      spacing: { before: 240, after: 60 },
      border: { bottom: { style: BorderStyle.SINGLE, size: 4, color: 'e4dece' } },
    }));

    (sec.lines || []).forEach(line => {
      if (!line) return;
      if (line.startsWith('•')) {
        children.push(new Paragraph({
          children: [new TextRun({ text: line, size: 20 })],
          indent: { left: 240 },
          spacing: { after: 60 },
        }));
      } else if (line.startsWith('__bold__')) {
        children.push(new Paragraph({
          children: [new TextRun({ text: line.replace('__bold__', ''), bold: true, size: 20 })],
          spacing: { after: 60 },
        }));
      } else {
        children.push(new Paragraph({
          children: [new TextRun({ text: line, size: 20 })],
          spacing: { after: 60 },
        }));
      }
    });
  });

  return new Document({ sections: [{ children }] });
}

/* ─────────────────────────────────────────────────────
   RESUME
───────────────────────────────────────────────────── */
function downloadPDF() {
  const s = resumeState;
  const name = s.contact.name || 'Resume';
  const allSkills = [...s.hardSkills, ...s.softSkills];
  const sections = [];

  if (s.summary) sections.push({ heading: 'Professional Summary', lines: [s.summary] });

  const expLines = [];
  s.experiences.filter(e => e.title).forEach(e => {
    expLines.push(`__bold__${e.title}${e.org ? ' — ' + e.org : ''}${e.dates ? '  |  ' + e.dates : ''}`);
    splitBullets(e.bullets).forEach(b => expLines.push('• ' + b));
    expLines.push('');
  });
  if (expLines.length) sections.push({ heading: 'Experience', lines: expLines });

  if (allSkills.length) sections.push({ heading: 'Skills', lines: [allSkills.join('  ·  ')] });

  const doc = buildPlainPDF(name, sections);
  // Contact block at top — inject before sections
  const { jsPDF } = window.jspdf;
  doc.save(`${name.replace(/\s+/g, '_')}_Resume.pdf`);
}

function downloadDOCX() {
  const s = resumeState;
  const name = s.contact.name || 'Resume';
  const allSkills = [...s.hardSkills, ...s.softSkills];
  const sections = [];

  const contactParts = [s.contact.email, s.contact.phone, s.contact.location].filter(Boolean);
  if (contactParts.length) sections.push({ heading: 'Contact', lines: [contactParts.join('  ·  ')] });
  if (s.summary) sections.push({ heading: 'Professional Summary', lines: [s.summary] });

  const expLines = [];
  s.experiences.filter(e => e.title).forEach(e => {
    expLines.push(`__bold__${e.title}${e.org ? ' — ' + e.org : ''}${e.dates ? '  |  ' + e.dates : ''}`);
    splitBullets(e.bullets).forEach(b => expLines.push('• ' + b));
    expLines.push('');
  });
  if (expLines.length) sections.push({ heading: 'Experience', lines: expLines });
  if (allSkills.length) sections.push({ heading: 'Skills', lines: [allSkills.join('  ·  ')] });

  const document = buildPlainDOCX(name, sections);
  docx.Packer.toBlob(document).then(blob => {
    saveAs(blob, `${name.replace(/\s+/g, '_')}_Resume.docx`);
  });
}

