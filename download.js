/* ─────────────────────────────────────────────────────
   PORTFOLIO
───────────────────────────────────────────────────── */
function buildPortfolioSections() {
  const s = portfolioState;
  const fieldLabel = FIELDS.find(f => f.id === s.field)?.label || s.field;
  const sections = [];
  if (fieldLabel) sections.push({ heading: 'Field', lines: [fieldLabel] });
  if (s.format) sections.push({ heading: 'Format', lines: [s.format] });
  s.projects.forEach((p, i) => {
    const lines = [];
    if (p.goal)     lines.push(`__bold__Goal:`, p.goal, '');
    if (p.problem)  lines.push(`__bold__Problem:`, p.problem, '');
    if (p.created)  lines.push(`__bold__What I Created:`, p.created, '');
    if (p.tools)    lines.push(`__bold__Tools Used:`, p.tools, '');
    if (p.outcome)  lines.push(`__bold__Outcome:`, p.outcome, '');
    if (p.learned)  lines.push(`__bold__What I Learned:`, p.learned, '');
    if (lines.length) sections.push({ heading: `Project ${i + 1}`, lines });
  });
  return sections;
}

