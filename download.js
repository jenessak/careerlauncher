/* ─────────────────────────────────────────────────────
   INTERVIEW PREP
───────────────────────────────────────────────────── */
function buildInterviewSections() {
  const s = interviewState;
  const fieldLabel = FIELDS.find(f => f.id === s.field)?.label || s.field;
  const commonQs = [
    'Tell me about yourself.',
    'Why should we hire you?',
    'What is your biggest strength?',
    'What is a weakness?',
    'Describe a time you solved a problem.',
  ];
  const sections = [];
  if (fieldLabel) sections.push({ heading: 'Field', lines: [fieldLabel] });

  const commonLines = [];
  commonQs.forEach((q, i) => {
    commonLines.push(`__bold__${q}`);
    commonLines.push(s.answers['q' + i] || '(No answer written yet)');
    commonLines.push('');
  });
  sections.push({ heading: 'Common Questions', lines: commonLines });

  const fieldQs = INTERVIEW_QUESTIONS[s.field] || INTERVIEW_QUESTIONS.general;
  const fieldLines = [];
  fieldQs.forEach((q, i) => {
    fieldLines.push(`__bold__${q}`);
    fieldLines.push(s.answers['fq' + i] || '(No answer written yet)');
    fieldLines.push('');
  });
  sections.push({ heading: `Field-Specific Questions — ${fieldLabel}`, lines: fieldLines });

  sections.push({ heading: 'Reminders', lines: [
    '• Send a thank-you email within 24 hours of your interview.',
    '• Practice answers out loud — not just in your head.',
    '• Arrive 10–15 minutes early (or log in early for virtual).',
    '• Bring 2 printed copies of your resume.',
  ]});
  return sections;
}

